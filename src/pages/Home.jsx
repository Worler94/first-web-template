function ImageSlider(props) {
    const settings = {
        dots: true,
        infinite: true,
        speed: 500,
        slidesToShow: 1,
        slidesToScroll: 1,
        autoplay: true,
        autoplaySpeed: 3000,
        arrows: false,
    };

    return (
        <Slider {...settings}>
            {props.images.map((image) => (
                <div key={image.id}>
                    <img src={image.src} alt={image.alt} />
                </div>
            ))}
        </Slider>
    );
}

